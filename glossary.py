# ──────────────────────────── Поиск ────────────────────────────

def _norm(s: str) -> str:
    return (s or "").strip().lower().replace("ё", "е")


def all_terms() -> list[tuple[str, dict]]:
    """Все статьи как (категория, статья)."""
    out = []
    for cat, items in YACHTING_GLOSSARY.items():
        for it in items:
            out.append((cat, it))
    return out


def total_count() -> int:
    return sum(len(v) for v in YACHTING_GLOSSARY.values())


def search(query: str, limit: int = 20) -> list[tuple[str, dict]]:
    """
    Поиск по термину и синонимам (подстрока), затем по определению.
    Возвращает список (категория, статья).
    """
    q = _norm(query)
    if not q:
        return []
    by_term, by_def = [], []
    for cat, it in all_terms():
        hay_term = _norm(it["term"]) + " " + " ".join(_norm(a) for a in it.get("aliases", []))
        if q in hay_term:
            by_term.append((cat, it))
        elif q in _norm(it.get("definition", "")):
            by_def.append((cat, it))
    return (by_term + by_def)[:limit]


def render(cat: str, it: dict) -> str:
    """HTML-карточка термина для отправки в Telegram."""
    title = it["term"]
    aliases = it.get("aliases") or []
    if aliases:
        title += " / " + ", ".join(aliases)
    icon = CATEGORY_ICONS.get(cat, "📚")
    text = f"{icon} <b>{title}</b>\n\n{it.get('definition', '')}"
    if it.get("example"):
        text += f"\n\n<i>Пример:</i> {it['example']}"
    return text
